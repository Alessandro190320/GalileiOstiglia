/* Inizializza il vettore con valori random.
@param int[] Vettore da utilizzare.
@param int Dimensione del vettore.
@return void.*/
void initVettore(int _v[], int _length);

/* Stampa un vettore passato come parametro.
@param int[] Vettore da utilizzare.
@param int Dimensione del vettore.
@return void.*/
void stampaVet(int _v[], int _length);

/* Calcola e restituisce la media dei valori di un vettore passato come parametro.
@param int[] Vettore da utilizzare.
@param int Dimensione del vettore.
@return float Media dei valori.*/
float mediaVet(int _v[], int _length);

/* Calcola e restituisce la varianza del vettore passato come parametro.
@param int[] Vettore da utilizzare.
@param int Dimensione del vettore.
@return float Varianza dei valori.*/
float varianzaVet(int _v[], int _length);